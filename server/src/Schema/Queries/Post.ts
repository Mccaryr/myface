import { GraphQLList } from "graphql"
import { PostType } from "../TypeDefs/Post"
import { Posts } from "../../Entities/Post";

interface IPost {
    content: string,
    user_id: number
}

export const GET_ALL_POSTS = {
    type: new GraphQLList(PostType),
    resolve(): Promise<IPost[]> {
        return Posts.find();
    }
}